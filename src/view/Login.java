    private void bt_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_loginActionPerformed
        String akun = inputLogin();
        String password = new String(pfPassword.getPassword()).trim();

        if (akun.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(
                    this,
                    "Email/nama dan password wajib diisi.",
                    "Login Gagal",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        try {
            UserDAO.LoginResult result = userDAO.login(akun, password);
            if (result != null) {
                if (bukaHalamanSetelahLogin(result)) {
                    simpanLoginJikaPerlu(akun);
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(
                        this,
                        "Email/nama atau password salah.",
                        "Login Gagal",
                        JOptionPane.ERROR_MESSAGE
                );
                pfPassword.setText("");
                pfPassword.requestFocus();
            }
        } catch (SQLException | RuntimeException ex) {
            JOptionPane.showMessageDialog(
                    this,
                    "Tidak bisa terhubung ke database.\n" + ex.getMessage(),
                    "Koneksi Gagal",
                    JOptionPane.ERROR_MESSAGE
            );
            pfPassword.setText("");
            pfPassword.requestFocus();
        }
    }//GEN-LAST:event_bt_loginActionPerformed

    private boolean bukaHalamanSetelahLogin(UserDAO.LoginResult result) {
        Integer idUser = result.idUser();
        Integer idAnggota = result.idAnggota();
        String nama = result.nama();
        String group = result.group();
        SesiLogin.masuk(idUser, idAnggota, nama, group);

        if ("Pengguna".equalsIgnoreCase(group)) {
            if (idAnggota == null) {
                JOptionPane.showMessageDialog(
                        this,
                        "Akun anggota belum dikaitkan dengan data anggota.\nHubungi Super Admin untuk mengisi No Anggota pada data user.",
                        "Login Anggota",
                        JOptionPane.WARNING_MESSAGE
                );
                SesiLogin.keluar();
                return false;
            }

            new DashboardAnggota(idAnggota, nama).setVisible(true);
            return true;
        }

        new Dashboard(nama, group).setVisible(true);
        return true;
    }

