    private void setupFrameDrag() {
        java.awt.event.MouseAdapter dragAdapter = new java.awt.event.MouseAdapter() {
            @Override
            public void mousePressed(java.awt.event.MouseEvent evt) {
                dragStartPoint = evt.getPoint();
            }

            @Override
            public void mouseDragged(java.awt.event.MouseEvent evt) {
                if (dragStartPoint == null) {
                    return;
                }

                java.awt.Point screenPoint = evt.getLocationOnScreen();
                setLocation(screenPoint.x - dragStartPoint.x, screenPoint.y - dragStartPoint.y);
            }
        };

        jPanel3.addMouseListener(dragAdapter);
        jPanel3.addMouseMotionListener(dragAdapter);
        jLabel1.addMouseListener(dragAdapter);
        jLabel1.addMouseMotionListener(dragAdapter);
    }

    private void setupIngatSaya() {
        boolean ingatLogin = PREFS.getBoolean(PREF_INGAT_LOGIN, false);
        cbingatsaya.setSelected(ingatLogin);
        if (ingatLogin) {
            jTextField1.setText(PREFS.get(PREF_AKUN_LOGIN, ""));
            pfPassword.requestFocus();
        }

        cbingatsaya.addActionListener(e -> {
            if (!cbingatsaya.isSelected()) {
                hapusLoginTersimpan();
            }
        });
    }

    private void simpanLoginJikaPerlu(String akun) {
        if (cbingatsaya.isSelected()) {
            PREFS.putBoolean(PREF_INGAT_LOGIN, true);
            PREFS.put(PREF_AKUN_LOGIN, akun);
        } else {
            hapusLoginTersimpan();
        }
    }

    private void hapusLoginTersimpan() {
        PREFS.putBoolean(PREF_INGAT_LOGIN, false);
        PREFS.remove(PREF_AKUN_LOGIN);
    }

