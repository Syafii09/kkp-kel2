    private DefaultTableModel tableDataToTableModel(LaporanDAO.TabelLaporan data) {
        DefaultTableModel model = new DefaultTableModel();

        for (String column : data.columns()) {
            model.addColumn(column);
        }

        for (List<Object> dataRow : data.rows()) {
            Object[] row = new Object[dataRow.size()];
            for (int column = 0; column < dataRow.size(); column++) {
                Object value = dataRow.get(column);
                row[column] = value instanceof BigDecimal ? formatRupiah((BigDecimal) value) : value;
            }
            model.addRow(row);
        }

        return model;
    }

    private void cetakLaporan() {
        if (tabelPreview.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Preview laporan terlebih dahulu.", "Cetak", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            if (previewJasperAktif && jasperPrintPreview != null) {
                cetakJasperPreview();
                return;
            }

            boolean dicetak = previewHtml.print();
            if (dicetak) {
                JOptionPane.showMessageDialog(this, "Laporan berhasil dikirim ke printer.");
            }
        } catch (PrinterException ex) {
            JOptionPane.showMessageDialog(this, "Gagal mencetak laporan.\n" + ex.getMessage(), "Cetak Gagal", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Gagal mencetak laporan Jasper.\n" + pesanException(ex), "Cetak Gagal", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void cetakJasperPreview() throws Exception {
        Class<?> jasperPrintClass = Class.forName("net.sf.jasperreports.engine.JasperPrint");
        Class<?> printManagerClass = Class.forName("net.sf.jasperreports.engine.JasperPrintManager");
        java.lang.reflect.Method printReport = printManagerClass.getMethod("printReport", jasperPrintClass, boolean.class);
        printReport.invoke(null, jasperPrintPreview, true);
    }

    private void exportLaporan() {
        if (tabelPreview.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Preview laporan terlebih dahulu.", "Export", JOptionPane.WARNING_MESSAGE);
            return;
        }

        JFileChooser chooser = new JFileChooser();
        chooser.setDialogTitle("Export Laporan");
        chooser.setSelectedFile(new File("laporan_" + jenisLaporan + ".csv"));
        chooser.setFileFilter(new FileNameExtensionFilter("CSV File (*.csv)", "csv"));

        if (chooser.showSaveDialog(this) != JFileChooser.APPROVE_OPTION) {
            return;
        }

        File file = chooser.getSelectedFile();
        if (!file.getName().toLowerCase().endsWith(".csv")) {
            file = new File(file.getParentFile(), file.getName() + ".csv");
        }

        try {
            exportTableToCsv(file);
            JOptionPane.showMessageDialog(this, "Laporan berhasil diexport.");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Gagal export laporan.\n" + ex.getMessage(), "Export Gagal", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void exportTableToCsv(File file) throws IOException {
        TableModel model = tabelPreview.getModel();

        try (BufferedWriter writer = new BufferedWriter(new FileWriter(file))) {
            for (int column = 0; column < model.getColumnCount(); column++) {
                writer.write(escapeCsv(model.getColumnName(column)));
                if (column < model.getColumnCount() - 1) writer.write(",");
            }
            writer.newLine();

            for (int row = 0; row < model.getRowCount(); row++) {
                for (int column = 0; column < model.getColumnCount(); column++) {
                    Object value = model.getValueAt(row, column);
                    writer.write(escapeCsv(value == null ? "" : value.toString()));
                    if (column < model.getColumnCount() - 1) writer.write(",");
                }
                writer.newLine();
            }
        }
    }

