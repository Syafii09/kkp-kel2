    private void loadDataAnggota() {
        loadDataAnggota("");
    }

    private void loadDataAnggota(String keyword) {
        DefaultTableModel model = (DefaultTableModel) tbAnggota.getModel();
        model.setRowCount(0);

        try {
            int nomor = 1;
            for (AnggotaDAO.AnggotaData anggota : anggotaDAO.search(keyword)) {
                tambahRowAnggota(model, nomor++, anggota);
            }
        } catch (SQLException | RuntimeException ex) {
            JOptionPane.showMessageDialog(
                    this,
                    "Gagal memuat data anggota.\n" + ex.getMessage(),
                    "Database Error",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }

    private void tambahRowAnggota(DefaultTableModel model, int nomor, AnggotaDAO.AnggotaData anggota) {
        Object[] row = new Object[model.getColumnCount()];

        for (int column = 0; column < model.getColumnCount(); column++) {
            String columnName = model.getColumnName(column).toLowerCase();

            if (columnName.equals("no")) {
                row[column] = nomor;
            } else if (columnName.contains("no. anggota") || columnName.contains("no anggota")) {
                row[column] = anggota.noAnggota();
            } else if (columnName.contains("nik")) {
                row[column] = anggota.nik();
            } else if (columnName.contains("nama")) {
                row[column] = anggota.nama();
            } else if (columnName.contains("tempat") || columnName.contains("lahir")) {
                row[column] = formatTempatTanggal(anggota.tempatLahir(), anggota.tanggalLahir());
            } else if (columnName.contains("hp")) {
                row[column] = anggota.noHp();
            } else if (columnName.contains("alamat")) {
                row[column] = anggota.alamat();
            } else if (columnName.contains("divisi") || columnName.contains("departemen")) {
                row[column] = anggota.divisi();
            } else if (columnName.contains("daftar")) {
                row[column] = anggota.tanggalDaftar();
            } else if (columnName.contains("status")) {
                row[column] = anggota.status();
            }
        }

        model.addRow(row);
    }

