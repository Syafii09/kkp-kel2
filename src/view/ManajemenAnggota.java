    private void tbAnggotaMouseClicked(java.awt.event.MouseEvent evt) {
        int selectedRow = tbAnggota.getSelectedRow();
        if (selectedRow == -1) {
            return;
        }

        int modelRow = tbAnggota.convertRowIndexToModel(selectedRow);
        String noAnggota = ambilNilaiTabel(modelRow, 1, "");
        tampilkanDetailAnggota(noAnggota);

        if (evt.getClickCount() == 2 && !noAnggota.isEmpty()) {
            bukaFormEditAnggota(noAnggota);
        }
    }

    private void tampilkanDetailAnggota(String noAnggota) {
        try {
            AnggotaDAO.DetailAnggota detail = anggotaDAO.getDetail(noAnggota);
            if (detail == null) {
                return;
            }

            tfNoanggota.setText(detail.noAnggota());
            setDetailNIK(detail.nik());
            tfNamaanggota.setText(detail.nama());
            tfTempatlahiranggota.setText(formatTempatTanggal(detail.tempatLahir(), detail.tanggalLahir()));
            tfAlamatanggota.setText(detail.alamat());
            tfNohpanggota.setText(detail.noHp());
            tfDivisianggota.setText(detail.divisi());
            tfStatusanggota.setText(detail.status());
            tfTanggaldaftaranggota.setText(String.valueOf(detail.tanggalDaftar()));
            tfJumlahsimpanananggota.setText(detail.totalSimpanan().toPlainString());
            tfJumlahPinjamananggota.setText(detail.totalPinjaman().toPlainString());
            setDetailAnggotaEnabled(false);
        } catch (SQLException | RuntimeException ex) {
            JOptionPane.showMessageDialog(
                    this,
                    "Gagal memuat detail anggota.\n" + ex.getMessage(),
                    "Database Error",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }

    private String formatTempatTanggal(String tempat, Date tanggal) {
        String tempatLahir = tempat == null ? "" : tempat;
        if (tanggal == null) {
            return tempatLahir;
        }

        String tanggalIndonesia = new SimpleDateFormat("dd MMMM yyyy", new Locale("id", "ID"))
                .format(tanggal)
                .toLowerCase(new Locale("id", "ID"));
        return tempatLahir + ", " + tanggalIndonesia;
    }

    private void exportTableToCsv(File file) throws IOException {
        TableModel model = tbAnggota.getModel();
        String delimiter = ";";

        try (BufferedWriter writer = new BufferedWriter(new OutputStreamWriter(
                new FileOutputStream(file), StandardCharsets.UTF_8))) {
            writer.write('\ufeff');
            writer.write("sep=");
            writer.write(delimiter);
            writer.newLine();

            for (int viewColumn = 0; viewColumn < tbAnggota.getColumnCount(); viewColumn++) {
                int modelColumn = tbAnggota.convertColumnIndexToModel(viewColumn);
                writer.write(escapeCsv(model.getColumnName(modelColumn), delimiter));
                if (viewColumn < tbAnggota.getColumnCount() - 1) {
                    writer.write(delimiter);
                }
            }
            writer.newLine();

            for (int viewRow = 0; viewRow < tbAnggota.getRowCount(); viewRow++) {
                int modelRow = tbAnggota.convertRowIndexToModel(viewRow);
                for (int viewColumn = 0; viewColumn < tbAnggota.getColumnCount(); viewColumn++) {
                    int modelColumn = tbAnggota.convertColumnIndexToModel(viewColumn);
                    Object value = model.getValueAt(modelRow, modelColumn);
                    String columnName = model.getColumnName(modelColumn);
                    writer.write(escapeCsv(formatCsvValue(value, columnName), delimiter));
                    if (viewColumn < tbAnggota.getColumnCount() - 1) {
                        writer.write(delimiter);
                    }
                }
                writer.newLine();
            }
        }
    }

    private String formatCsvValue(Object value, String columnName) {
        if (value instanceof Date tanggal) {
            return new SimpleDateFormat("dd-MM-yyyy").format(tanggal);
        }

        String text = value == null ? "" : value.toString().trim();
        return isExcelTextColumn(columnName) ? toExcelText(text) : text;
    }

    private boolean isExcelTextColumn(String columnName) {
        String name = columnName == null ? "" : columnName.toLowerCase();
        return name.contains("nik") || name.contains("hp") || name.contains("no. anggota")
                || name.contains("no anggota");
    }

    private String toExcelText(String text) {
        if (text.isEmpty()) {
            return "";
        }

        return "=\"" + text.replace("\"", "\"\"") + "\"";
    }

    private String escapeCsv(String value, String delimiter) {
        String text = value == null ? "" : value;
        boolean perluQuote = text.contains(delimiter) || text.contains("\"")
                || text.contains("\n") || text.contains("\r");
        text = text.replace("\"", "\"\"");
        return perluQuote ? "\"" + text + "\"" : text;
    }

    private String pesanException(Throwable throwable) {
        Throwable cause = throwable;
        while (cause.getCause() != null) {
            cause = cause.getCause();
        }

        String message = cause.getMessage();
        if (message == null || message.isBlank()) {
            return cause.getClass().getName();
        }
        return cause.getClass().getName() + ": " + message;
    }

