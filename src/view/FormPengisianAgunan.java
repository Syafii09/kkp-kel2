    private void setupForm(Integer idAgunan, Runnable onSaved) {
        this.idAgunan = idAgunan;
        this.onSaved = onSaved;
        setTitle(idAgunan == null ? "Tambah Agunan" : "Edit Agunan");
        setLocationRelativeTo(null);

        getRootPane().registerKeyboardAction(
                e -> simpan(),
                javax.swing.KeyStroke.getKeyStroke(KeyEvent.VK_S, KeyEvent.CTRL_DOWN_MASK),
                javax.swing.JComponent.WHEN_IN_FOCUSED_WINDOW
        );
        getRootPane().registerKeyboardAction(
                e -> dispose(),
                javax.swing.KeyStroke.getKeyStroke(KeyEvent.VK_ESCAPE, 0),
                javax.swing.JComponent.WHEN_IN_FOCUSED_WINDOW
        );
        btSave.addActionListener(e -> simpan());
        btCancel.addActionListener(e -> dispose());

        muatPeminjam();
        calCalonanggota.setDate(new Date());
        if (idAgunan != null) {
            muatDataEdit(idAgunan);
        }
    }

    private void muatPeminjam() {
        try {
            List<AgunanDAO.PinjamanPilihan> pinjaman = agunanDAO.getPinjamanAktif();
            cbNamaPeminjam.removeAllItems();
            cbNamaPeminjam.addItem("-- Pilih Peminjam --");
            for (AgunanDAO.PinjamanPilihan item : pinjaman) {
                cbNamaPeminjam.addItem(item.toString());
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Tidak bisa memuat data peminjam.\n" + ex.getMessage());
        }
    }

    private void muatDataEdit(int idAgunan) {
        try {
            AgunanDAO.AgunanData data = agunanDAO.getById(idAgunan);
            if (data == null) {
                JOptionPane.showMessageDialog(this, "Data agunan tidak ditemukan.");
                dispose();
                return;
            }

            pilihPeminjam(data.noPinjaman());
            cbJenisAgunan.setSelectedItem(data.jenisAgunan());
            tfNilaiTaksir.setText(PengaturanSistem.formatUang(data.nilaiTaksir()));
            calCalonanggota.setDate(data.tanggal());
            cbStatus.setSelectedItem(data.status());
            taDeskripsi.setText(data.deskripsi());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Tidak bisa memuat data agunan.\n" + ex.getMessage());
        }
    }

    private void pilihPeminjam(String noPinjaman) {
        for (int i = 0; i < cbNamaPeminjam.getItemCount(); i++) {
            String item = cbNamaPeminjam.getItemAt(i);
            if (item != null && item.startsWith(noPinjaman + " -")) {
                cbNamaPeminjam.setSelectedIndex(i);
                return;
            }
        }
    }

    private void simpan() {
        String noPinjaman = ambilNoPinjaman();
        String jenisAgunan = (String) cbJenisAgunan.getSelectedItem();

        if (noPinjaman == null) {
            JOptionPane.showMessageDialog(this, "Pilih peminjam terlebih dahulu.");
            return;
        }
        if (jenisAgunan == null || jenisAgunan.startsWith("--")) {
            JOptionPane.showMessageDialog(this, "Pilih jenis agunan terlebih dahulu.");
            return;
        }
        if (calCalonanggota.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Tanggal wajib diisi.");
            return;
        }

        try {
            BigDecimal nilaiTaksir = PengaturanSistem.parseNominal(tfNilaiTaksir.getText());
            AgunanDAO.AgunanData data = new AgunanDAO.AgunanData(
                    idAgunan,
                    noPinjaman,
                    jenisAgunan,
                    nilaiTaksir,
                    calCalonanggota.getDate(),
                    cbStatus.getSelectedItem().toString(),
                    taDeskripsi.getText().trim()
            );

            if (idAgunan == null) {
                agunanDAO.insert(data);
            } else {
                agunanDAO.update(data);
            }

            JOptionPane.showMessageDialog(this, "Data agunan berhasil disimpan.");
            if (onSaved != null) {
                onSaved.run();
            }
            dispose();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data agunan.\n" + ex.getMessage());
        }
    }

    private String ambilNoPinjaman() {
        String selected = (String) cbNamaPeminjam.getSelectedItem();
        if (selected == null || selected.startsWith("--")) {
            return null;
        }
        int separator = selected.indexOf(" -");
        return separator > 0 ? selected.substring(0, separator) : selected;
    }

