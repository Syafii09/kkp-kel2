    private String tableToHtml() {
        TableModel model = tabelPreview.getModel();
        List<Integer> columns = kolomPreview();
        StringBuilder html = new StringBuilder("<table><thead><tr>");

        html.append("<th>No.</th>");
        for (Integer column : columns) {
            html.append("<th>").append(escapeHtml(model.getColumnName(column))).append("</th>");
        }
        html.append("</tr></thead><tbody>");

        for (int row = 0; row < model.getRowCount(); row++) {
            html.append("<tr>");
            html.append("<td class=\"no\">").append(row + 1).append("</td>");
            for (Integer column : columns) {
                Object value = model.getValueAt(row, column);
                String text = value == null ? "" : value.toString();
                html.append("<td");
                if (isKolomUang(model.getColumnName(column))) {
                    html.append(" class=\"money\"");
                }
                html.append(">").append(escapeHtml(text)).append("</td>");
            }
            html.append("</tr>");
        }

        html.append(barisTotal(columns));
        html.append("</tbody></table>");
        return html.toString();
    }

    private List<Integer> kolomPreview() {
        TableModel model = tabelPreview.getModel();
        List<Integer> columns = new ArrayList<>();

        for (int column = 0; column < model.getColumnCount(); column++) {
            String name = model.getColumnName(column);
            if ("simpanan".equals(jenisLaporan)
                    && ("Tanggal".equalsIgnoreCase(name) || "Keterangan".equalsIgnoreCase(name))) {
                continue;
            }
            columns.add(column);
        }

        return columns;
    }

    private String barisTotal(List<Integer> columns) {
        String[] targetColumns = kolomTotal();
        if (targetColumns.length == 0 || tabelPreview.getRowCount() == 0) {
            return "";
        }

        int totalCount = 0;
        for (String target : targetColumns) {
            if (cariKolom(target) >= 0) {
                totalCount++;
            }
        }

        StringBuilder html = new StringBuilder("<tr class=\"total\">");
        int headerCount = columns.size() + 1;
        int colspan = Math.max(1, headerCount - Math.max(1, totalCount));
        html.append("<td colspan=\"").append(colspan).append("\" class=\"money\">").append(escapeHtml(labelTotal())).append("</td>");

        int renderedTotals = 0;
        for (String target : targetColumns) {
            int column = cariKolom(target);
            if (column >= 0) {
                html.append("<td class=\"money\">").append(escapeHtml(formatRupiah(totalKolom(column)))).append("</td>");
                renderedTotals++;
            }
        }

        if (renderedTotals == 0) {
            html.append("<td></td>");
        }
        html.append("</tr>");
        return html.toString();
    }

