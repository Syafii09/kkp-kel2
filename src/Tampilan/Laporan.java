    private int cariKolom(String namaKolom) {
        TableModel model = tabelPreview.getModel();
        for (int column = 0; column < model.getColumnCount(); column++) {
            if (namaKolom.equalsIgnoreCase(model.getColumnName(column))) {
                return column;
            }
        }
        return -1;
    }

    private BigDecimal totalKolom(int column) {
        BigDecimal total = BigDecimal.ZERO;
        TableModel model = tabelPreview.getModel();
        for (int row = 0; row < model.getRowCount(); row++) {
            total = total.add(parseRupiah(model.getValueAt(row, column)));
        }
        return total;
    }

    private BigDecimal parseRupiah(Object value) {
        if (value == null) {
            return BigDecimal.ZERO;
        }
        String angka = value.toString().replaceAll("[^0-9-]", "");
        if (angka.isBlank() || "-".equals(angka)) {
            return BigDecimal.ZERO;
        }
        return new BigDecimal(angka);
    }

    private boolean isKolomUang(String name) {
        return "Nominal".equalsIgnoreCase(name)
                || "Saldo".equalsIgnoreCase(name)
                || "Jumlah".equalsIgnoreCase(name)
                || "Debet".equalsIgnoreCase(name)
                || "Kredit".equalsIgnoreCase(name)
                || "Angsuran/Bulan".equalsIgnoreCase(name);
    }

    private String teksPeriode() {
        Date awal = dtTglawal.getDate();
        Date akhir = dtTglakhir.getDate();
        SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");

        if (awal == null && akhir == null) {
            return "Semua Periode";
        }
        if (awal == null) {
            return "Periode s/d " + format.format(akhir);
        }
        if (akhir == null) {
            return "Periode " + format.format(awal) + " s/d sekarang";
        }
        return "Periode " + format.format(awal) + " s/d " + format.format(akhir);
    }

    private String teksTanggalTandaTangan() {
        Locale indonesia = new Locale("id", "ID");
        return "Pemalang, " + new SimpleDateFormat("dd MMMM yyyy", indonesia).format(new Date());
    }

