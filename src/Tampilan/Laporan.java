    private void exportLaporan() {
        if (tabelPreview.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Preview laporan terlebih dahulu.", "Export", JOptionPane.WARNING_MESSAGE);
            return;
        }

        JFileChooser chooser = new JFileChooser();
        chooser.setDialogTitle("Export Laporan");
        chooser.setSelectedFile(new File("laporan_" + jenisLaporan + ".csv"));
        chooser.setFileFilter(new FileNameExtensionFilter("CSV File (*.csv)", "csv"));

        if (chooser.showSaveDialog(this) != JFileChooser.APPROVE_OPTION) {
            return;
        }

        File file = chooser.getSelectedFile();
        if (!file.getName().toLowerCase().endsWith(".csv")) {
            file = new File(file.getParentFile(), file.getName() + ".csv");
        }

        try {
            exportTableToCsv(file);
            JOptionPane.showMessageDialog(this, "Laporan berhasil diexport.");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Gagal export laporan.\n" + ex.getMessage(), "Export Gagal", JOptionPane.ERROR_MESSAGE);
        }
    }

    private File buatFileHtmlLaporan() throws IOException {
        File file = File.createTempFile("laporan_" + jenisLaporan + "_", ".html");
        file.deleteOnExit();

        try (BufferedWriter writer = new BufferedWriter(new FileWriter(file))) {
            writer.write(buatHtmlLaporan(true));
        }

        return file;
    }

    private void exportTableToCsv(File file) throws IOException {
        TableModel model = tabelPreview.getModel();

        try (BufferedWriter writer = new BufferedWriter(new FileWriter(file))) {
            for (int column = 0; column < model.getColumnCount(); column++) {
                writer.write(escapeCsv(model.getColumnName(column)));
                if (column < model.getColumnCount() - 1) writer.write(",");
            }
            writer.newLine();

            for (int row = 0; row < model.getRowCount(); row++) {
                for (int column = 0; column < model.getColumnCount(); column++) {
                    Object value = model.getValueAt(row, column);
                    writer.write(escapeCsv(value == null ? "" : value.toString()));
                    if (column < model.getColumnCount() - 1) writer.write(",");
                }
                writer.newLine();
            }
        }
    }

